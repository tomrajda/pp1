# Calculation of the area and circumference of a circle
import math

# determine radius and PI
radius = 5
pi = math.pi

# calculate area 
area = pi * radius ** 2

# calculate circumference 
circumference = 2 * pi * radius

# display results 
print(f"Area = {round(area, 2)}"
    f"\nCircumference = {round(circumference, 2)}")
