def month(n):
    """
    Returns month
    """
    months = [
        "january",
        "february",
        "march",
        "april",
        "may",
        "june",
        "july",
        "august",
        "september",
        "october",
        "november",
        "december"
        ]

    return months[n-1]

print(month(7)) 